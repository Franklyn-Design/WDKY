  var NODE_KEY_MAP = new WeakMap();

  function normalizeText(s) {
    return (s || '').replace(/\s+/g, ' ').trim();
  }

  function buildReverseZhMap() {
    var map = {};
    Object.keys(T.zh).forEach(function (key) {
      var val = T.zh[key];
      if (typeof val !== 'string') return;
      if (val.indexOf('<') !== -1) return;
      var n = normalizeText(val);
      if (n && !map[n]) map[n] = key;
    });
    return map;
  }

  var reverseZh = buildReverseZhMap();

  function getLangDict(lang) {
    return T[lang] || T.zh;
  }

  function getText(dict, key) {
    return dict[key] !== undefined ? dict[key] : (T.en[key] !== undefined ? T.en[key] : T.zh[key]);
  }

  function applyStaticNodeTranslations(dict) {
    var walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    var node;
    while ((node = walker.nextNode())) {
      var parent = node.parentElement;
      if (!parent) continue;
      if (parent.closest('script,style,noscript')) continue;
      if (parent.closest('[data-i18n],[data-i18n-html]')) continue;

      var key = NODE_KEY_MAP.get(node);
      if (!key) {
        var trimmed = normalizeText(node.nodeValue);
        key = reverseZh[trimmed];
        if (key) NODE_KEY_MAP.set(node, key);
      }

      if (!key) continue;
      var translated = getText(dict, key);
      if (translated === undefined) continue;

      var raw = node.nodeValue || '';
      var t = raw.trim();
      if (!t) continue;
      var leading = raw.slice(0, raw.indexOf(t));
      var trailing = raw.slice(raw.indexOf(t) + t.length);
      node.nodeValue = leading + translated + trailing;
    }
  }

  function applyLang(lang) {
    var dict = getLangDict(lang);

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var val = getText(dict, key);
      if (val !== undefined) el.textContent = val;
    });

    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      var val = getText(dict, key);
      if (val !== undefined) el.innerHTML = val;
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-ph');
      var val = getText(dict, key);
      if (val !== undefined) el.placeholder = val;
    });

    document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-aria');
      var val = getText(dict, key);
      if (val !== undefined) el.setAttribute('aria-label', val);
    });

    applyStaticNodeTranslations(dict);

    document.documentElement.lang = (lang === 'zh') ? 'zh-CN' : lang;

    var pageKey = document.documentElement.getAttribute('data-page-key');
    if (pageKey) {
      var title = getText(dict, 'meta.' + pageKey + '.title');
      var desc = getText(dict, 'meta.' + pageKey + '.desc');
      if (title) document.title = title;
      var metaDesc = document.querySelector('meta[name="description"]');
      if (metaDesc && desc) metaDesc.setAttribute('content', desc);
    }

    var select = document.getElementById('lang-select');
    if (select) select.value = lang;
  }

  function init() {
    var supported = ['zh', 'en', 'ko', 'ja'];
    var saved = localStorage.getItem('wdky-lang') || 'zh';
    if (supported.indexOf(saved) === -1) saved = 'zh';

    var select = document.getElementById('lang-select');
    if (select) {
      select.addEventListener('change', function () {
        var next = select.value;
        localStorage.setItem('wdky-lang', next);
        applyLang(next);
      });
    }

    applyLang(saved);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
